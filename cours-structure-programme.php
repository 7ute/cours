<h3 id="algostructure-dun-programme">Structure d'un programme</h3>
<div class="contenu">
<pre class="javascript">
    <span class="hljs-keyword">PROGRAMME</span> nom_du_programme

    <span class="hljs-keyword">CONST</span>
        // ici la déclaration des constantes
        TUTU ← 20 : entier

    <span class="hljs-keyword">VAR</span>
        // ici la déclaration des variables
        toto : entier
        titi,tata : chaines

    <span class="hljs-keyword">DEBUT</span>
        /* le corps du programme
        c’est ici que nous effectuerons les principales opérations */
        toto ← 3
        titi ← <span class="hljs-string">«loulou?»</span>

    <span class="hljs-keyword">FIN</span>
</pre>
    Voila comment se structure un programme.<br />
    Il a un nom (de préférence en rapport avec son action), ici «nom_du_programme».<br />
    Si l’on ne fait pas de declaration de variables ou de constantes, les mots clefs «VAR» et «CONST» peuvent être omis.<br /><br />

    Le code sur une ligne précédée de // ou encadré de /* et */ ne sera pas éxécuté. Ce sont des commentaires.<br />
    Entre les mots clef «DEBUT» et «FIN», nous pourrons effectuer toutes nos opérations.<br />
</div>
